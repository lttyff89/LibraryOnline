using LibraryApp.Services;

var builder = WebApplication.CreateBuilder(args);

// Leer la configuración de la URL base desde el archivo appsettings.json
var apiBaseUrl = builder.Configuration.GetValue<string>("ApiSettings:BaseUrl");

// Configura el HttpClient para la API, puedes cambiar la URL base a la de tu API
builder.Services.AddHttpClient<BookService>(client =>
{
    client.BaseAddress = new Uri(apiBaseUrl);  // URL base de tu API
});

builder.Services.AddControllersWithViews();

var app = builder.Build();

if (app.Environment.IsDevelopment())
{
    app.UseDeveloperExceptionPage();
}
else
{
    app.UseExceptionHandler("/Home/Error");
    app.UseHsts();
}

app.UseHttpsRedirection();
app.UseStaticFiles();
app.UseRouting();

app.MapControllerRoute(
    name: "default",
    pattern: "{controller=Home}/{action=Index}/{id?}");

app.Run();


